package pt.haslab.htapbench.distributions;

public class UniformDistribution extends Distribution{

    private int max;
    private int min;
    private int interval;
    
    public UniformDistribution(int min, int max) {
        super("uniform");
        this.min = min;
        this.max = max;
        this.interval = max-min+1;
    }
    
    @Override
    public int nextInt(){
        int ret=Utils.random().nextInt(interval)+min;
        return ret;
    }

    public double mean() {
        return ((double)((long)(max + (long)max))) / 2.0;
    }
    
    
    
    
}
